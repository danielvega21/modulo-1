public class Pares_2_al_100 {
    public static void main(String[] args) {
        System.out.println("Números pares del 2 al 100:");

        // Usar un ciclo for para imprimir números pares
        for (int i = 2; i <= 100; i += 2) {
            System.out.println(i);
        }
    }
}

